class CardModel:
    def __init__(self, name, type, firstAppearance, levelOfFear, size, danger, rarity, deletedAt):
        self.name = name
        self.type = type
        self.firstAppearance = firstAppearance
        self.levelOfFear = levelOfFear
        self.size = size
        self.danger = danger
        self.rarity = rarity
        self.deletedAt = deletedAt